
public class TiposNumericos {

	public static void main(String[] args) {
		

			Byte numeroByte = 1; //Armazena de -128 até 127 - se declarar 130 ja dá problema
			Short numeroShort = 1; // de -32768 até 32767
			Integer numeroInteger = 1; // de 2 elevado até a trigésima primeira potencia: 2^31 = -2147483648 até 2^31 = 2147483648 - 1 = 2147483647
			Long numeroLong = 1L; //2^63 = -9,223372037x10 até 2^63 9,223372037x10 -1 = 9,223372036x10
			Float numeroFloat = 1.0f;
			Double numeroDouble = 1.0;
	}

}
